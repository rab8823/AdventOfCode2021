    private static readonly Dictionary<char, int> Counts = new()
    {
        ['('] = 0,
        ['['] = 0,
        ['{'] = 0,
        ['<'] = 0,
    };

    private static readonly Stack<char> Stack = new();

    public override void SolvePart1()
    {
        var score = 0;
        var valid = new List<string>();
        var corrupt = new List<string>();
        var incomplete = new List<string>();
        foreach (var line in Utils.SplitLines(Data))
        {
            char currentChunk = default;
            bool hasCorruptChunk = false;
            foreach (var c in line)
            {
                switch (c)
                {
                    case '(':
                    case '[':
                    case '{':
                    case '<':
                        currentChunk = c;
                        Stack.Push(c);
                        break;
                    case ')':
                    case ']':
                    case '}':
                    case '>':
                        if (currentChunk == default)
                        {
                            // 
                            throw new InvalidOperationException("mismatched chunk");
                        }
                        else
                        {
                            if (GetExpectedChunk(c) != currentChunk)
                            {
                                score += GetScore(c);
                                // corrupted chunk
                                hasCorruptChunk = true;
                                break;
                            }
                            else
                            {
                                Stack.Pop();
                                currentChunk = Stack.Peek();
                            }
                        }
                        break;
                    default:
                        throw new InvalidOperationException("bad line");
                }
                if (hasCorruptChunk)
                {
                    break;
                }
            }
            if (hasCorruptChunk)
            {
                corrupt.Add(line);
                //just go to next line
                continue;
            }
            else if (Stack.Any())
            {
                // incomplete line
                incomplete.Add(line);
            }
            else
            {
                // valid line
                valid.Add(line);
            }
        }
        Console.WriteLine(score);
    }

    private static int GetScore(char c) => c switch
    {
        ')' => 3,
        ']' => 57,
        '}' => 1197,
        '>' => 25137,
        _ => throw new InvalidOperationException("invalid chunk character")
    };

    private static long GetScore2(Stack<char> stack, long startingScore)
    {
        if (stack.TryPop(out char c))
        {
            var score = 5 * startingScore + c switch
            {
                '(' => 1,
                '[' => 2,
                '{' => 3,
                '<' => 4,
                _ => throw new InvalidOperationException("invalid char")
            };
            return GetScore2(stack, score);
        }
        else
        {
            return startingScore;
        }
    }


    private static char GetExpectedChunk(char c) => c switch
    {
        '(' => ')',
        ')' => '(',
        '[' => ']',
        ']' => '[',
        '{' => '}',
        '}' => '{',
        '<' => '>',
        '>' => '<',
        _ => throw new InvalidOperationException("invalid chunk character")
    };

    public override void SolvePart2()
    {
        var score = 0;
        var scores = new List<long>();
        var valid = new List<string>();
        var corrupt = new List<string>();
        var incomplete = new List<string>();
        foreach (var line in Utils.SplitLines(Data))
        {
            char currentChunk = default;
            bool hasCorruptChunk = false;
            foreach (var c in line)
            {
                switch (c)
                {
                    case '(':
                    case '[':
                    case '{':
                    case '<':
                        currentChunk = c;
                        Stack.Push(c);
                        break;
                    case ')':
                    case ']':
                    case '}':
                    case '>':
                        if (currentChunk == default)
                        {
                            // 
                            throw new InvalidOperationException("mismatched chunk");
                        }
                        else
                        {
                            if (GetExpectedChunk(c) != currentChunk)
                            {
                                score += GetScore(c);
                                // corrupted chunk
                                hasCorruptChunk = true;
                                break;
                            }
                            else
                            {
                                Stack.Pop();
                                Stack.TryPeek(out currentChunk);
                            }
                        }
                        break;
                    default:
                        throw new InvalidOperationException("bad line");
                }
                if (hasCorruptChunk)
                {
                    break;
                }
            }
            if (hasCorruptChunk)
            {
                corrupt.Add(line);
            }
            else if (Stack.Any())
            {
                // incomplete line
                incomplete.Add(line);
                scores.Add(GetScore2(Stack, 0));
            }
            else
            {
                // valid line
                valid.Add(line);
            }
            Stack.Clear();
        }

        scores.Sort();
        Console.WriteLine(scores[scores.Count / 2]);
    }
